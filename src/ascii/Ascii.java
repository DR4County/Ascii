package ascii;

/**
 *
 * @author dreyes
 */
public class Ascii {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        System.out.println("  .-\"-.");
        System.out.println(" /|6 6|\\");
        System.out.println(" {/(_0_)\\}");
        System.out.println(" _/ ^ \\_");
        System.out.println(" (/ /^\\ \\)-'");
        System.out.println(" \"\"' '\"\"");
        System.out.println("\n FIDO");
        System.out.println("");
        //Ascii of a cat
        System.out.println("  /'\\__.__/'\\ ");
        System.out.println("~| 0   ,  0  |~_");
        System.out.println(" ~\\  ./\\.   /~/ _\\");
        System.out.println("  / .|.  .|.\\| |");
        System.out.println("  | |||  ||| | | ");
        System.out.println("  \\ ,,,  ,,, / /");
        // Ascii of a chair that the cat is sitting on
        System.out.println("   _____________");
        System.out.println("  |-=-=-=-=-=-=-|");
        System.out.println("  || |       | ||");
        System.out.println("  || |       | ||");
        System.out.println("  '' '       ' ''");

    }
    
}
